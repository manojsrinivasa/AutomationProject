package language.basics;

public class ConditionalSwitchCase {

	public static void main(String[] args) {
		
		String color = "red";
		
		switch (color)	{
		
		case "red":
		System.out.println("Passion, Love, Anger");
		break;
			
		case "blue":
		System.out.println("Calmness, Trust, Loyalty");
		break;
		
		case "Green":
		System.out.println("Growth, Nature, Money");
		break;
		
		case "Yellow":
		System.out.println("Happiness, Energy, Optimism");
		break;
		
		case "Purple":
		System.out.println("Royalty, Creativity, Luxury");
		
		default:
		System.out.println("Enter a valid color");
		
		
		}
		
		
		
		
		
		
	

	}

}
